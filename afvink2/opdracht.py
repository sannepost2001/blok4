def proteinseqmaken(seq):
    """Maakt van de DNA sequentie een proteine sequentie
    Input: seq = string van de nucleotide
    Output: protein = string van aminozuren
    """
    protein = ""
    # maak codons dus 3 nucleotide check wat de proteine er toegevoegd moet worden en start op ATG het startcodon
    for i in range(0, len(seq), 3):
        codon = seq[i:i + 3]
        if len(codon) == 3:
            protein += trna[codon]
    return protein


